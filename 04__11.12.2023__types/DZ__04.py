import math as m

print('============== Zadanie 1 ==============')
# 1. –£—á–∏–º—Å—è —Ä–∞–±–æ—Ç–∞—Ç—å —Å —Ñ–æ—Ä–º—É–ª–∞–º–∏.
# –ò—Å–ø–æ–ª—å–∑—É—è –º–æ–¥—É–ª—å math, –≤—ã—á–∏—Å–ª–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏—è –ø–æ —Å–ª–µ–¥—É—é—â–∏–º
# —Ñ–æ—Ä–º—É–ª–∞–º:

a = float(input("–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ: a\n"))
b = float(input("–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ: b\n"))
x = float(input("–í–≤–µ–¥–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ: x\n"))
# a, b, x = 1, 2, 3

# a)
y = (pow(a, 2) / 3 +
     (pow(a, 2)+4) / b +
     m.sqrt(pow(a, 2)+4) / 4 +
     m.sqrt(pow(pow(a, 2)+4, 3)) / 4)
print(f'–í–∞—Ä–∏–∞–Ω—Ç a) y = {y:.4f}')
# b)
y = m.cos(x) + m.sin(x)
print(f'–í–∞—Ä–∏–∞–Ω—Ç b) y = {y:.4f}')
# c)
y = m.cbrt(pow(m.cos(pow(x, 2)), 2) + pow(m.sin(2*x-1), 2))
print(f'–í–∞—Ä–∏–∞–Ω—Ç c) y = {y:.4f}')
# d)
y = 5 * x + 3 * pow(x, 2) * m.sqrt(1 + pow(m.sin(x), 2))
print(f'–í–∞—Ä–∏–∞–Ω—Ç d) y = {y:.4f}\n')


print('============== Zadanie 2 ==============')
# 2. ¬´–ò–≥—Ä–∞–ª —Å –∫—Ä–µ–¥–∏—Ç–∞–º–∏ ‚Äì –ø—Ä–æ–∏–≥—Ä–∞–ª¬ª
# m ‚Äì –µ–∂–µ–º–µ—Å—è—á–Ω–∞—è –≤—ã–ø–ª–∞—Ç–∞
# i ‚Äì –≥–æ–¥–æ–≤–∞—è –ø—Ä–æ—Ü–µ–Ω—Ç–Ω–∞—è —Å—Ç–∞–≤–∫–∞
# p ‚Äì –º–µ—Å—è—á–Ω–∞—è –ø—Ä–æ—Ü–µ–Ω—Ç–Ω–∞—è —Å—Ç–∞–≤–∫–∞
# s ‚Äì —Å—É–º–º–∞ –∑–∞–π–º–∞
# n ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–µ—Å—è—Ü–µ–≤, –Ω–∞
# –∫–æ—Ç–æ—Ä—ã–µ –≤–∑—è—Ç –∫—Ä–µ–¥–∏—Ç –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–æ–¥–∏—Ç —Å –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã i, s –∏ n, –ø—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞
# —Ä–∞—Å—Å—á–∏—Ç–∞—Ç—å —Ä–∞–∑–º–µ—Ä –µ–∂–µ–º–µ—Å—è—á–Ω–æ–π –≤—ã–ø–ª–∞—Ç—ã –ø–æ —Ñ–æ—Ä–º—É–ª–µ, –∞ —Ç–∞–∫–∂–µ –Ω–∞–π—Ç–∏,
# —Å–∫–æ–ª—å–∫–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—Å–µ–≥–æ –∑–∞–ø–ª–∞—Ç–∏—Ç –±–∞–Ω–∫—É –∏ —Å–∫–æ–ª—å–∫–æ —Å–æ—Å—Ç–∞–≤–∏—Ç –ø–µ—Ä–µ–ø–ª–∞—Ç–∞. –°–¥–µ–ª–∞—Ç—å
# –≤–≤–æ–¥/–≤—ã–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö –∫—Ä–∞—Å–∏–≤—ã–º –∏–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–æ–º
i = float(input("–í–≤–µ–¥–∏—Ç–µ –≥–æ–¥–æ–≤—É—é –ø—Ä–æ—Ü–µ–Ω—Ç–Ω—É—é —Å—Ç–∞–≤–∫—É: i\n"))
s = float(input("–í–≤–µ–¥–∏—Ç–µ —Å—É–º–º—É –∑–∞–π–º–∞: s\n"))
n = float(input("–í–≤–µ–¥–∏—Ç–µ —Å—Ä–æ–∫ –∫—Ä–µ–¥–∏—Ç–∞ –≤ –º–µ—Å—è—Ü–∞—Ö: n\n"))
# i, s, n = 24, 5000, 12

p = i / (12 * 100)
M = s * p * pow(1 + p, n) / (pow(1 + p, n) - 1)
print(f'–ï–∂–µ–º–µ—Å—è—á–Ω–∞—è –≤—ã–ø–ª–∞—Ç–∞: {M:.2f}')
print(f'–í—Å–µ–≥–æ –ø—Ä–∏–¥—ë—Ç—Å—è –∑–∞–ø–ª–∞—Ç–∏—Ç—å –±–∞–Ω–∫—É: {M * n:.2f}')
print(f'–ü–µ—Ä–µ–ø–ª–∞—Ç–∞ –ø–æ –∫—Ä–µ–¥–∏—Ç—É —Å–æ—Å—Ç–∞–≤–∏—Ç: {M * n - s:.2f}\n')


print('============== Zadanie 3 ==============')
# 3. ¬´–ò–Ω—Ç–µ—Ä—Å—Ç–µ–ª–ª–∞—Ä¬ª
# L - –¥–ª–∏–Ω–∞ –≥–æ–¥–∞ –Ω–∞ –ø–ª–∞–Ω–µ—Ç–µ
# R ‚Äì —Ä–∞–¥–∏—É—Å –æ—Ä–±–∏—Ç—ã –ø–ª–∞–Ω–µ—Ç—ã (–º–ª–Ω.–∫–º.)
# ùíó ‚Äì –æ—Ä–±–∏—Ç–∞–ª—å–Ω–∞—è —Å–∫–æ—Ä–æ—Å—Ç—å (–∫–º/—á)
# –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–æ–¥–∏—Ç —Å –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –¥–≤—É—Ö –ø–ª–∞–Ω–µ—Ç–∞—Ö: —Ä–∞–¥–∏—É—Å –∏ –æ—Ä–±–∏—Ç–∞–ª—å–Ω—É—é —Å–∫–æ—Ä–æ—Å—Ç—å. –ü—Ä–æ–≥—Ä–∞–º–º–∞
# –¥–æ–ª–∂–Ω–∞ –ø–æ—Å—á–∏—Ç–∞—Ç—å –∏ –≤—ã–≤–µ—Å—Ç–∏ –¥–ª–∏–Ω—É –≥–æ–¥–∞ –Ω–∞ –ø–µ—Ä–≤–æ–π –∏ –≤—Ç–æ—Ä–æ–π –ø–ª–∞–Ω–µ—Ç–∞—Ö, –∞ —Ç–∞–∫–∂–µ –≤—ã–≤–µ—Å—Ç–∏, –ø—Ä–∞–≤–¥–∞ –ª–∏, —á—Ç–æ –Ω–∞ –ø–µ—Ä–≤–æ–π –ø–ª–∞–Ω–µ—Ç–µ
# –≥–æ–¥ –¥–ª–∏–Ω–Ω–µ–µ, —á–µ–º –Ω–∞ –≤—Ç–æ—Ä–æ–π. –°–¥–µ–ª–∞—Ç—å –≤–≤–æ–¥/–≤—ã–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö –∫—Ä–∞—Å–∏–≤—ã–º –∏–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–æ–º
print("–í–≤–µ–¥–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ 1-–π –ø–ª–∞–Ω–µ—Ç—ã:")
R1 = float(input("—Ä–∞–¥–∏—É—Å –≤ –º–ª–Ω.–∫–º: R1\n"))
v1 = float(input("–æ—Ä–±–∏—Ç–∞–ª—å–Ω–∞—è —Å–∫–æ—Ä–æ—Å—Ç—å –≤ –∫–º/—á: v1\n"))
print("–í–≤–µ–¥–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ 2-–π –ø–ª–∞–Ω–µ—Ç—ã:")
R2 = float(input("—Ä–∞–¥–∏—É—Å –≤ –º–ª–Ω.–∫–º: R2\n"))
v2 = float(input("–æ—Ä–±–∏—Ç–∞–ª—å–Ω–∞—è —Å–∫–æ—Ä–æ—Å—Ç—å –≤ –∫–º/—á: v2\n"))
# R1, v1 = 100, 100
# R2, v2 = 200, 100

L1 = 2 * R1 * m.pi / v1
L2 = 2 * R2 * m.pi / v2
print(f'–î–ª–∏–Ω–∞ –≥–æ–¥–∞:\n–Ω–∞ 1-–π –ø–ª–∞–Ω–µ—Ç–µ: {L1 * 1000:.2f} —Ç—ã—Å.—á = {L1 * 1000000/ 24:.2f} –∑–µ–º–Ω—ã—Ö –¥–Ω–µ–π\n'
      f'–Ω–∞ 2-–π –ø–ª–∞–Ω–µ—Ç–µ: {L2 * 1000:.2f} —Ç—ã—Å.—á = {L2 * 1000000/ 24:.2f} –∑–µ–º–Ω—ã—Ö –¥–Ω–µ–π\n')
if L1 > L2:
    print(f'–î–ª–∏–Ω–∞ –≥–æ–¥–∞ –Ω–∞ 1-–π –ø–ª–∞–Ω–µ—Ç–µ –±–æ–ª—å—à–µ, —á–µ–º –Ω–∞ –≤—Ç–æ—Ä–æ–π –Ω–∞ {(L1 - L2) * 1000:.2f} —Ç—ã—Å.—á\n')
elif L1 == L2:
    print(f'–î–ª–∏–Ω—ã –≥–æ–¥–∞ –Ω–∞ –æ–±–æ–∏—Ö –ø–ª–∞–Ω–µ—Ç–∞—Ö –æ–¥–∏–Ω–∞–∫–æ–≤—ã')
else:
    print(f'–î–ª–∏–Ω–∞ –≥–æ–¥–∞ –Ω–∞ 1-–π –ø–ª–∞–Ω–µ—Ç–µ –º–µ–Ω—å—à–µ, —á–µ–º –Ω–∞ –≤—Ç–æ—Ä–æ–π –Ω–∞ {(L2 - L1) * 1000:.2f} —Ç—ã—Å.—á\n')
